n=int(input("enter your percentage :"))
if(n>=90):
    print("your grade is A+")
elif(n>=80):
    print("your grade is A")
elif(n>=70):
    print("your grade is B")
elif(n>=60):
    print("your grade is C")
elif(n>=50):
    print("your grade is D")
elif(n>=40):
    print("your grade is E")
elif(n<40):
    print("you FAILED")
else:
    print('wrong percentage entered')
    
